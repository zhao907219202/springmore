package org.springmore.commons.map;

/**
 * 经纬度
 * @author 唐延波
 * @date 2014-4-28 下午6:15:26
 */
public class Location {

	/**
	 * 经度
	 */
	private String lng;
	
	
	/**
	 * 纬度
	 */
	private String lat;
	

	public String getLng() {
		return lng;
	}

	public void setLng(String lng) {
		this.lng = lng;
	}

	public String getLat() {
		return lat;
	}

	public void setLat(String lat) {
		this.lat = lat;
	}
	
	
}
